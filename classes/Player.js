class Player {
  constructor(firstName, lastName ,points, rebounds, assists, steals, threes, blocks, fgs, fts) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.points = points;
    this.rebounds = rebounds; 
    this.assists = assists;
    this.steals = steals; 
    this.threes = threes; 
    this.blocks = blocks; 
    this.fgs = fgs; 
    this.fts = fts;
  } 
}


